function [rgbTriplet1] = draw_h(input1)
% beautiful color
% 提供更好看的颜色，类似于头文件的作用
switch input1
    case 1
        rgbTriplet1 = 0.01*round(110*[ ...
            0, 114, 189;...
            217, 83, 25;...
            255/1.1, 177, 32;... %old:237
            126, 47, 142;...
            119, 172, 48]/255); %加亮度的蓝、橙、黄、紫、绿
    case 2
        rgbTriplet1 = 0.01*round(100*[ ...
            126 206 244;...
            210 204 161;...
            206 190 190;...
            237 177 131;...
            239 111 108]/255); %莫兰迪配色：天蓝、浅绿、灰、橘黄、粉红
    case 3
        rgbTriplet1 = 0.01*round(100*[ ...
            255 000 000;...
            000 255 000;...
            000 000 255;...
            230 200 000;...
            000 000 000]/255); %高对比度：红、绿、蓝、黄(不能用255,255,00，不然太亮了看不清)、黑
end
end